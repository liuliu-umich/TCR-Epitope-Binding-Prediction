#Amino acid sequence distributions
def get_aac(seq):
  protein_class = PyProtein.PyProtein(seq)
  aac = protein_class.GetAAComp()
  return aac

def get_dpc(seq):
  protein_class = PyProtein.PyProtein(seq)
  dpc = protein_class.GetDPComp()
  return dpc

def get_paac(seq):
  max_lambda = 10
  seq_len = len(seq)
  if seq_len <= max_lambda:
      safe_lambda = seq_len - 1 if seq_len > 0 else 0
  else:
      safe_lambda = max_lambda
  protein_class = PyProtein.PyProtein(seq)
  paac = protein_class.GetPAAC(lamda=safe_lambda)
  return paac

def get_apaac(seq):
  max_lambda = 10
  seq_len = len(seq)
  if seq_len <= max_lambda:
      safe_lambda = seq_len - 1 if seq_len > 0 else 0
  else:
      safe_lambda = max_lambda
  protein_class = PyProtein.PyProtein(seq)
  apaac = protein_class.GetAPAAC(lamda=safe_lambda)
  return apaac

