#Amino acid sequence distributions
def get_aac(seq):
  """
  Converts an amino acid sequence into a sparse matrix.

  Args:
      seq: an amino acid sequence

  Returns:
      dictionary of amino acid breakdown
  """
  protein_class = PyProtein.PyProtein(seq)
  aac = protein_class.GetAAComp()
  return aac

def get_dpc(seq):
  """
  Converts an amino acid sequence into a sparse matrix
    based on amino acid sequence pairs.

  Args:
      seq: an amino acid sequence

  Returns:
      dictionary of amino acid dimer breakdown
  """
  protein_class = PyProtein.PyProtein(seq)
  dpc = protein_class.GetDPComp()
  return dpc

#Composition features
def get_CTD(seq):
  """
  Reports physical and chemical properties of an
    amino acid sequence.

  Args:
      seq: an amino acid sequence

  Returns:
      dictionary of amino acid characteristics
  """
  composition_features = CTD.CalculateCTD(seq)
  return composition_features

#Function for feature generation
def get_select_features(df, func):
  """
  Takes a dataframe and applies a function to generate a new dataframe
    with the specified features.

  Args:
      df (pd.DataFrame):: target dataframe
      func: function to apply

  Returns:
      pd.DataFrame: A new DataFrame containing features generated by the
      specified function.
  """


  #Get CDR3 features
  df = df.copy().reset_index(drop=True)
  df_target = df['cdr3'].apply(func)
  df_func = pd.json_normalize(df_target)
  if func == get_CTD:
    df_func = df_func.add_prefix('CDR3_chem_')
  elif func == get_dpc:
    df_func = df_func.add_prefix('CDR3_dpComp_')
  else:
    df_func = df_func.add_prefix('CDR3_aaComp_')
  df_func = df_func.reset_index(drop=True)
  #pn_df1 = pd.merge(df, df_func, left_index=True,
  #                  right_index=True, how = 'left')

  #Get epitope features
  df_target1 = df['antigen.epitope'].apply(func)
  df_func1 = pd.json_normalize(df_target1)
  if func == get_CTD:
    df_func1 = df_func1.add_prefix('Epitope_chem_')
  elif func == get_dpc:
    df_func1 = df_func1.add_prefix('Epitope_dpComp_')
  else:
    df_func1 = df_func1.add_prefix('Epitope_aaComp_')
  df_func1 = df_func1.reset_index(drop=True)
  pn_df2 = pd.concat([df, df_func, df_func1], axis=1)
  #pd.merge(pn_df1, df_func1, left_index=True,
           #         right_index=True, how = 'left')

  return pn_df2.reset_index(drop=True)

# #Create dataframes for new features
aa_df = get_select_features(df, get_aac)
dp_df = get_select_features(df, get_dpc)
ctd_df = get_select_features(df, get_CTD)

