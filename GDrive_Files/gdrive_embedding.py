def get_peptide_embeddings(sequences, tokenizer=tokenizer, model=model, device=device):
    """
    Generates ESM embeddings for a list of peptide sequences.
    """
    # Create a datasets.Dataset from the sequences for easy batch processing
    dataset = Dataset.from_dict({"sequence": sequences})

    # Tokenize the sequences
    def tokenize_function(examples):
        return tokenizer(examples["sequence"], padding=True, truncation=True)

    tokenized_dataset = dataset.map(tokenize_function, batched=True)
    tokenized_dataset.set_format(type="torch", columns=["input_ids", "attention_mask"])

    all_embeddings = []

    # Process sequences in batches
    for i in range(0, len(tokenized_dataset), 8): # Batch size of 8
        print(f"Processing batch {i // 8 + 1}/{len(tokenized_dataset) // 8 + 1}")
        batch = tokenized_dataset[i:i+8]
        input_ids = batch['input_ids'].to(device)
        attention_mask = batch['attention_mask'].to(device)

        with torch.no_grad():
            outputs = model(input_ids, attention_mask=attention_mask, output_hidden_states=True)

        # Get the hidden states from the last layer.
        # hidden_states[33] corresponds to the 33rd layer for the esm2_t33 model.
        last_hidden_state = outputs.hidden_states[33]

        # Take the mean of the embeddings for the non-padding tokens.
        # This gives a single vector representation for each peptide.
        for seq_idx in range(last_hidden_state.shape[0]):
            embedding = last_hidden_state[seq_idx, 1: -1].mean(dim=0).cpu().numpy()
            all_embeddings.append(embedding)

    return all_embeddings

