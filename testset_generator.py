def positive_negative_split(df):
    """
    Creates a new dataframe contain a positive and negative population.
        Negative population will be generated by randomly shuffling the
        defined population's cdr3 vs epitope sequence.

    Args:
        df (pd.DataFrame): The DataFrame from which to sample.

    Returns:
        pd.DataFrame: A new DataFrame containing the positive and negative
                      populations.
    """

    pos_df = df.sample(frac=0.5, random_state=42)
    neg_df = df.drop(pos_df.index)

    pos_df['Population'] = 'Positive'
    pos_df['Binding'] = 1

    original_neg = neg_df['cdr3'].values.copy()
    shuffled_neg = original_neg
    while np.any(shuffled_neg == original_neg):
        shuffled_neg = np.random.shuffle(original_neg)
    neg_df['cdr3'] = neg_df['cdr3'].replace(shuffled_neg)

    neg_df['Population'] = 'Negative'
    neg_df['Binding'] = 0


    df_return = pd.concat([pos_df, neg_df])
    df_return = df_return.fillna("Unknown")

    return df_return

