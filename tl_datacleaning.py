def positive_negative_split(df, use_unknowns = True):
    """
    Creates a new dataframe contain a positive and negative population.
        Negative population will be generated by randomly shuffling the
        defined population's cdr3 vs epitope sequence.

    Args:
        df (pd.DataFrame): The DataFrame from which to sample.
        use_unknowns: If True, values with a vdjdb.score of 0 will be used
                           to create the negative population. If False, values
                           with a vdjdb.score of 1 and 2 will be used.

    Returns:
        pd.DataFrame: A new DataFrame containing the positive and negative
                      populations.
    """
    if use_unknowns == True:
        pos_df = df[df['vdjdb.score'] >= 1]
        neg_df = df[df['vdjdb.score'] == 0]
        neg_df_sample = neg_df.sample(n=len(pos_df), random_state=42)
    else:
        pos_df = df[df['vdjdb.score'] == 3]
        neg_df = df[df['vdjdb.score'].isin([1,2])]
        neg_df_sample = neg_df

    #pos_df['Population'] = 'Positive'
    pos_df['Binding'] = 1

    neg_df_sample = neg_df.sample(n=len(pos_df), random_state=42)
    original_neg = neg_df_sample['cdr3'].values.copy()
    shuffled_neg = original_neg
    while np.any(shuffled_neg == original_neg):
        shuffled_neg = np.random.shuffle(original_neg)
    neg_df_sample['cdr3'] = neg_df_sample['cdr3'].replace(shuffled_neg)

    #neg_df_sample['Population'] = 'Negative'
    neg_df_sample['Binding'] = 0


    df_return = pd.concat([pos_df, neg_df_sample])
    df_return = df_return.drop('vdjdb.score', axis =1)
    df_return = df_return.dropna()
    df_return = df_return.reset_index(drop=True)

    return df_return

