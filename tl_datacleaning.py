def load_df(file_path, columns_to_keep=None, columns_duplicate = None):
    """
    Opens a file, converts it to a pandas DataFrame, removes specified columns,
    and returns the cleaned DataFrame.

    Args:
        file_path (str): The path to file.
        columns_to_keep (list, optional): A list of column names to keep.
            Defaults to None
        columns_duplicate (list, optional): A list of column names to remove
            duplicates frome. Defaults to None

    Returns:
        pandas.DataFrame: The cleaned DataFrame.
    """

    if not os.path.exists(file_path):
        raise FileNotFoundError(f"The file '{file_path}' does not exist.")


    file_ext = os.path.splitext(file_path)[1].lower()
    if file_ext == '.csv':
        df = pd.read_csv(file_path)
    elif file_ext == '.txt':
        df = pd.read_csv(file_path, sep='\t')
    elif file_ext == '.xlsx':
        df = pd.read_excel(file_path)
    else:
        raise ValueError("Unsupported file format. Only .csv and .xlsx files are supported.")

    # Select human samples only
    try:
        df = df[df['species'] == 'HomoSapiens']
    except:
        df = df[df['Species'] == 'HomoSapiens']

    # Remove unwanted columns if specified
    if columns_to_keep:
        df = df[columns_to_keep]

    #sort based on score
    df = df.sort_values(by='vdjdb.score', ascending=False)

    #Replace NA with Unknown where necessary
    df = df.dropna()
    if columns_duplicate:
        df.drop_duplicates(subset=columns_duplicate, keep='first', inplace=True)
    else:
        df.drop_duplicates()
    df = df.reset_index(drop=True)

    return df

